// Helper function to convert roles array to positions array (backend now expects array)
export const rolesToPositions = (roles: string[]): string[] => {
  if (roles.length === 0) return [];
  // Return all valid positions from roles array
  return roles.filter((role) => isValidPosition(role));
};

// Legacy function for backward compatibility - returns first position
export const rolesToPosition = (roles: string[]): string | undefined => {
  const positions = rolesToPositions(roles);
  return positions.length > 0 ? positions[0] : undefined;
};
